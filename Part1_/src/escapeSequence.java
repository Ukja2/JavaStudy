
public class escapeSequence {

	public static void main(String[] args) {
		//특수 문자
		
		System.out.println("a\nb"); // 줄바꿈
		System.out.println("a\tb"); // tab
		System.out.println("c:\\Java"); // 백슬래쉬
		System.out.println("a\"b\"c"); //큰따옴표 (\")
		System.out.println("a\'b\'c"); // 작은 따옴표 (\')
	}

}
